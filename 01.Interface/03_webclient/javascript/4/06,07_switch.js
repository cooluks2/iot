//switch

var menu = 1;

switch (Number(menu)) {
    case 1:
        console.log("전화를 겁니다.");
        break;
    case 2:
        console.log("문자를 보냅니다.");
        break;
    case 3:
        console.log("영상 통화를 연결합니다.");
        break;
    default:
        console.log("잘못 입력하셨습니다.");
        break;
}

var menu = "짬뽕";
switch (menu) {
case "짜장면":
console.log("수타면으로 준비해 드리겠습니다.");
break;
case "짬뽕":
console.log("시원핚 홍합 짬뽕입니다.");
break;
case "탕수육":
console.log("소고기 탕수육입니다.");
break;
default:
console.log("군만두나 드세요.");
break;
}

