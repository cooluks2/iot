//fallthrough

var yoil = "월";

switch (yoil) {
    case "월":
        console.log("일주일의 시작입니다.");
        break;
    case "화":
    case "수":
    case "목":
        console.log("열심히 일해야 할 때입니다.");
        break;
    case "금":
        console.log("불타는 금요일 보내세요.");
        break;
    case "토":
    case "일":
        console.log("편안핚 주말 보내세요.");
        break;
}

/*
일주일의 시작입니다.
*/